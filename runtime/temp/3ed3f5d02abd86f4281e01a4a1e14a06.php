    function checkSku(){
        var value_id = '';
        var goods_id = "<?php echo $goods['goods_id'];?>";
        $('.des_choice').find('.checked').each(function(){
            value_id += $(this).attr('value_id') +',';
        });
        $.ajax({
            url:"<?php echo url('Product/checkSku'); ?>",
            data:'value_id='+value_id+'&goods_id='+goods_id,
            type:'post',
            dataType:'json',
            success:function( info ){
                if( info.status == 1000 ){
                    var url = "<?php echo url('Product/productDetail' , ['id' => '__sku_id__'] ); ?>";
                    url = url.replace( '__sku_id__' , info.data.sku_id) ;
                    window.location.href =url;
                }else{
                    layui.layer.msg(info.msg);
                }
            }
        })


    }

