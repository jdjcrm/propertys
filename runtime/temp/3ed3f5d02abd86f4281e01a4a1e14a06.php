    var number = parseInt( $('.n_ipt').val());
    number = number - 1;
    if( number > stock ){
        number = stock;
    }
    if(  1 > number  ){
        $(this).prop('disabled' , true );
        number = 1;
    }
    $('.n_ipt').val(number);
})

$('.n_ipt').blur(function(){
    var stock = $('#stock').text();
    var buy_number = parseInt( $('.n_ipt').val());
    if( isNaN(buy_number ) ){
        $('.n_ipt').val(1);
        return false;
    }
    if( buy_number > stock ){
        $('.n_ipt').val(stock)
    }else if(  1 > buy_number ) {
        $('.n_ipt').val(1);
    }else{
        $('.n_ipt').val(buy_number);
    }
});


