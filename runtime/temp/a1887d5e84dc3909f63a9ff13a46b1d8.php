    function getAjaxPage( type , order_field , order_type ){
        // 获取是否选中品牌
        var brand_id = $('.brand').find('.now').attr('brand_id');
        var price = $('.price').find('.now').text();


        var data = '';
        if( brand_id != '' && brand_id != undefined ){
            data = 'brand_id='+brand_id;
        }

        if( price != '' && price != undefined ){
            if(data != '' ){
                data += '&';
            }
            data += 'price='+price;
        }

        if( order_field != '' ){
            if(data != '' ){
                data += '&';
            }
            data += 'order_field='+order_field+'&order_type='+order_type;
        }

        // 点击品牌 价格跟着改变
        if( type == 1 ){

            $.ajax({
                url:"<?php echo url('product/showMoneySelect',['cid' => request() -> param('cid')] ); ?>",
                data:data,
                type:'post',
                dataType:'json',
                success:function( html_info ){
                    $('#price_select').html(html_info);
                }
            });
        }

        // ajax请求筛选对应品牌下边的商品
        $.ajax({
            url:"<?php echo url('product/productList',['cid' => request() -> param('cid')] ); ?>",
            data:data,
            type:'post',
            dataType:'json',
            success:function( html_info ){
                $('#productlist').html(html_info);
            }
        })
    }



